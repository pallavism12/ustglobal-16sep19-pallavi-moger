package com.ustglobal.Clases;

public class Student12 {
	int id;
	String name;
	double percentage;
	public Student12(int id, String name, double percentage) {
		super();
		this.id = id;
		this.name = name;
		this.percentage = percentage;
	}
	@Override
	public String toString() {
		return "Student12 [id=" + id + ", name=" + name + ", percentage=" + percentage + "]";
	}
	
	
	

}
